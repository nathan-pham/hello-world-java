public class PrimitiveTypes {
    public static void main(String[] args) {
        int intMin = Integer.MIN_VALUE;
        int intMax = Integer.MAX_VALUE;
        System.out.println("Minimum Integer: " + intMin);
        System.out.println("Maximum Integer: " + intMax);

        byte byteMin = Byte.MIN_VALUE;
        byte byteMax = Byte.MAX_VALUE;
        System.out.println("Minimum Byte: " + byteMin);
        System.out.println("Maximum Byte: " + byteMax);

        short shortMin = Short.MIN_VALUE;
        short shortMax = Short.MAX_VALUE;
        System.out.println("Minimum Short: " + shortMin);
        System.out.println("Maximum Short: " + shortMax);
    
        long exampleLong = 100L;
        long longMin = Long.MIN_VALUE;
        long longMax = Long.MAX_VALUE;
        System.out.println("Minimum long: " + longMin);
        System.out.println("Maximum long: " + longMax);
        System.out.println("Example Long: " + exampleLong);

        
    }
}
